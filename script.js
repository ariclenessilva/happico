// Cursor trail effect (optional, for extra wow factor)
const createCursorTrail = () => {
    let lastX = 0;
    let lastY = 0;
    let isMoving = false;
    
    document.addEventListener('mousemove', (e) => {
        if (!isMoving) {
            isMoving = true;
            lastX = e.clientX;
            lastY = e.clientY;
            
            setTimeout(() => {
                isMoving = false;
            }, 50);
        }
    });
};

// Initialize cursor trail on desktop only
if (window.innerWidth > 768) {
    createCursorTrail();
}

